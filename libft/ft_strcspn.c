#include "libft.h"

size_t	ft_strcspn(const char *s1, const char *s2)
{
	size_t	i;

	if (s1 == NULL || s2 == NULL)
		return (0);
	i = 0;
	while (s1[i] != s2[i] && (s1[i] != '\0' && s2[i] != '\0'))
		i++;
	return (i);
}
