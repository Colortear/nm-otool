char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	new;

	i = 0;
	new = (char)c;
	while (s[i] != '\0')
		i++;
	while (i >= 0)
	{
		if (s[i] == new)
			return ((char *)&s[i]);
		i--;
	}
	return (0);
}
